/**
 * Get a translated UI string. Supports {0}, {1} placeholders.
 */
export function ui(key: TranslationKey, lang: string, ...args: (string | number)[]): string {
  const dict = translations[lang] ?? translations.en;
  let str = dict[key] ?? translations.en[key] ?? key;
  args.forEach((arg, i) => {
    str = str.replace(`{${i}}`, String(arg));
  });
  return str;
}

/**
 * Get the font family for a language code.
 */
export function getFontFamily(langCode: string): string {
  if (langCode === "ar") return "'Noto Sans Arabic', 'Inter', sans-serif";
  if (langCode === "ti" || langCode === "am") return "'Noto Sans Ethiopic', 'Inter', sans-serif";
  return "'Inter', sans-serif";
}
